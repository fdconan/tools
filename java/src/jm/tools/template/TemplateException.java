package jm.tools.template;

public class TemplateException extends Exception {
	private static final long serialVersionUID = -6098842196273542057L;

	public TemplateException() {
		super();
	}

	public TemplateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TemplateException(String arg0) {
		super(arg0);
	}

	public TemplateException(Throwable arg0) {
		super(arg0);
	}

}
